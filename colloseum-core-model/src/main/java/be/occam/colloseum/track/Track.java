package be.occam.colloseum.track;

public class Track {
	
	protected String id;
	protected String title;

	public String getId() {
		return id;
	}

	public Track setId(String id) {
		this.id = id;
		return this;
	}

	public String getTitle() {
		return title;
	}

	public Track setTitle(String title) {
		this.title = title;
		return this;
	}
	
	
	
}
